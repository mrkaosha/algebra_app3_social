class MainApp extends StatefulWidget {
  const MainApp({super.key});

  @override
  State<MainApp> createState() => _MainAppState();
}

class _MainAppState extends State<MainApp> {
  double x = 0.0;
  double y = 0.0;
  double canvasWidth = 500;
  double canvasHeight = 500;
  double gridSize = 25;
  double margins = 0.05; //the margin size as a percent of canvasWidth
  _StdToSlopeIntData eqns = _StdToSlopeIntData();
  Map currentEquation = {};
  bool drawCursor = false;
  bool tapUp = true;

  @override
  _MainAppState() {
    currentEquation = eqns.getNextEquation();
    _resetDataList();
    print(currentEquation);
  }

  List<List<bool>> dataList = [];

  void _resetDataList() {
    dataList = List.generate(
      (canvasHeight / gridSize as int) + 1,
      (i) => List.generate(
        (canvasWidth / gridSize as int) + 1,
        (j) => false,
        growable: false,
      ),
      growable: false,
    );
  }

  void _updateLocation(PointerEvent details) {
    setState(() {
      x = (details.localPosition.dx / gridSize).round() * gridSize;
      if (x < margins * canvasWidth) {
        x = margins * canvasWidth;
      } else if (x > canvasWidth * (1 + margins)) {
        x = canvasWidth * (1 + margins);
      }
      y = (details.localPosition.dy / gridSize).round() * gridSize;
      if (y < margins * canvasHeight) {
        y = margins * canvasHeight;
      } else if (y > canvasHeight * (1 + margins)) {
        y = canvasHeight * (1 + margins);
      }
      if (!tapUp) {
        drawCursor = true;
      } else {
        tapUp = false;
      }
    });
  }

  void _toggleDataPoint(TapDownDetails details) {
    x = ((details.localPosition.dx - margins * canvasWidth) / gridSize)
            .round() *
        gridSize;
    y = ((details.localPosition.dy - margins * canvasHeight) / gridSize)
            .round() *
        gridSize;
    int i = ((canvasWidth - y) / gridSize) as int;
    if (i < 0) {
      i = 0;
    } else if (i > dataList.length - 1) {
      i = dataList.length - 1;
    }
    int j = x / gridSize as int;
    if (j < 0) {
      j = 0;
    } else if (j > dataList[0].length - 1) {
      j = dataList.length - 1;
    }
    print("$i, $j");
    setState(() {
      dataList[i][j] ? dataList[i][j] = false : dataList[i][j] = true;
      drawCursor = false;
    });
    //print(dataList);
  }

  void nextEquation() {
    setState(() => currentEquation = eqns.getNextEquation());
    print(currentEquation);
  }

  bool checkAnswer() {
    bool correct = true;
    int numPoints = 0;
    double offset =
        -10.0; // ****should I put this offset in the class global variables?****
    for (int i = 0; i <= (canvasHeight / gridSize as int); i++) {
      // grab the dots for the data points
      var r = dataList[i];
      r.asMap().forEach((index, d) {
        if (d) {
          numPoints++;
          print((i + offset) * currentEquation['den']);
          print(currentEquation['num'] * (index + offset) +
              currentEquation['yint'] * currentEquation['den']);
        }
        if (d &&
            ((i + offset) * currentEquation['den'] !=
                currentEquation['num'] * (index + offset) +
                    currentEquation['yint'] * currentEquation['den'])) {
          correct = false;
        }
      });
    }
    print(correct && numPoints > 1);
    return (correct && numPoints > 1);
  }

  @override
  Widget build(BuildContext context) {
    String eqnString = "";
    eqnString += (-1 * currentEquation['num']).sign < 0 ? "−" : "";
    eqnString += (-1 * currentEquation['num']).abs() != 1
        ? currentEquation['num'].abs().toString()
        : "";

    eqnString += "x";

    eqnString += (currentEquation['den']).sign < 0 ? " − " : " + ";
    eqnString += (currentEquation['den']).abs() != 1
        ? currentEquation['den'].abs().toString()
        : "";

    eqnString += "y = ";

    eqnString +=
        (currentEquation['den'] * currentEquation['yint']).sign < 0 ? "−" : "";
    eqnString +=
        (currentEquation['den'] * currentEquation['yint']).abs().toString();
    eqnString += ":";

    var theme = Theme.of(context);

    return MaterialApp(
      home: Scaffold(
        body: ListView(
          children: [
            Align(
              child: LoginButton(),
            ),
            Align(
              child: Padding(
                padding: const EdgeInsets.all(20.0),
                child: Text(
                  "Graph $eqnString",
                  style: const TextStyle(fontSize: 18),
                ),
              ),
            ),
            Align(
              child: SingleChildScrollView(
                scrollDirection: Axis.horizontal,
                child: Padding(
                  padding: const EdgeInsets.all(40.0),
                  child: SizedBox(
                    width: canvasWidth * (1.0 + margins * 2),
                    height: canvasHeight * (1.0 + margins * 2),
                    child: MouseRegion(
                      //onHover: _updateLocation,
                      onExit: (p) {
                        setState(() => drawCursor = false);
                      },
                      cursor: SystemMouseCursors.click,
                      child: GestureDetector(
                        onTapDown: _toggleDataPoint,
                        //onTapUp: (d) => setState(() => {tapUp = true}),
                        //onLongPressDown: (d) => print('long'),
                        child: CustomPaint(
                          foregroundPainter: CursorPainter(x, y, drawCursor),
                          /**
                           * DONE: add the margins into the GridPainter
                           * also the gesture detector needs to know the margins to
                           * trim them off the edges
                           * x, y = clickEvent offset minus the margin size
                           * there needs to be a flag to detect when the cursor/clickEvent
                           * is in the margins (if outside the left margin set x = 0 + offest, if
                           * outside the top margin set y = height/gridSize + offset)
                           */
                          painter: GridPainter(canvasWidth, canvasHeight,
                              gridSize, margins, dataList),
                        ),
                      ),
                    ),
                  ),
                ),
              ),
            ),
            Align(
              child: ActionButtons(
                checkAnswer: checkAnswer,
                nextEquation: nextEquation,
                currentEquation: currentEquation,
              ),
            ),
            TextButton(
              onPressed: () {
                setState(() => _resetDataList());
              },
              child: const Text("Clear graph"),
            ),
            const Padding(
              padding: EdgeInsets.only(bottom: 240.0),
            ),
            Card(
              color: theme.colorScheme.primary,
              child: const Padding(
                padding: EdgeInsets.all(8.0),
                child: Align(
                  child: Text(
                    "South Hills Academy",
